DOSUSB
DOSUSB to sterownik open source dla niektórych kontrolerów USB.
Obecnie współpracuje tylko z kontrolerami UHCI, czyli USB 1.1.
Brak jeszcze obsługi USB 2.0 i USB 3.0.

