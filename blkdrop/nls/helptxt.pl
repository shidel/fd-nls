BlockDrop wymaga myszy i komputera 386 lub lepszego z grafiką VGA. Nie
zdążyłem jeszcze dodać dźwięku. Kiedyś się uda.

Podstawowa idea gry nie jest zbyt skomplikowana. W zależności od poziomu,
pewna liczba kolorowych i specjalnych bloków jest umieszczana losowo w
podajniku bloków.

Podgląd podajnika znajduje się w prawym dolnym rogu ekranu. Pokazuje do 20
następnych rzędów bloków. Gdy blok dotrze do dolnej części podajnika, jest
wyświetlany bardziej szczegółowo nad strefą zrzutu.

W przeciwieństwie do niektórych innych gier, bloki nad strefą zrzutu spadają
pionowo w dół i nie można ich przesuwać. W końcu lądują na stosie. Dopóki nie
wylądują, są niewrażliwe na jakiekolwiek interakcje.

Nie pozwól, aby bloki piętrzyły się zbyt wysoko. Zablokują one podajnik, a Ty
stracisz życie, co zmusi Cię do ponownego rozpoczęcia poziomu.

Ale nie martw się, gdy bloki znajdą się już na stosie, istnieje kilka sposobów
na ich usunięcie i skrócenie stosu. Zazwyczaj można kliknąć na grupę trzech
lub więcej bloków, aby je usunąć. Im więcej bloków w tej grupie, tym więcej
punktów otrzymasz.

Istnieją twarde bloki (wyglądają trochę jak czerwony X), których nie można
kliknąć, aby je usunąć, niezależnie od tego, ile ich się styka. Możesz się ich
pozbyć tylko detonując w pobliżu standardową bombę. Spowoduje to usunięcie
wszystkich bloków w bezpośrednim sąsiedztwie bomby.

Istnieją również specjalne bloki z kolorowymi bombami, które usuną ze stosu
wszystkie bloki tego samego koloru. Oraz inne rodzaje bloków do zadań specjalnych.

Gdy bloki są usuwane ze stosu, spadające bloki celowo wahają się przez chwilę,
aby pozwolić stosowi na całkowite osunięcie się przed wylądowaniem.

O... Jeśli dotrzesz do wystarczająco wysokiego poziomu i przypadkiem zobaczysz
czaszkę z piszczelami, nie klikaj na nią. To natychmiastowa śmierć. To jedyny
blok, w który nigdy nie chcesz kliknąć.

Podczas gry możesz używać -/+, aby czasem spowolnić lub przyspieszyć grę. Nie
ma to wpływu na Twój wynik i jest przeznaczone dla doświadczonych graczy,
którzy mogliby się nudzić na niższych, wolniejszych poziomach.

Na początku poziomu możesz nacisnąć Tab, aby pominąć ten poziom. Ale ostrzegam,
poziomy stają się tylko szybsze i trudniejsze.

Obecnie, gdy bloki są usuwane ze stosu, osiadają one tylko w kierunku dolnym.
Rozważam jednak, aby przesuwały się również do wewnątrz, aby wypełnić puste
kolumny. Myślę też, że gra może stawać się zbyt trudna zbyt szybko. Pewne
korekty tempa trudności mogą być wskazane. Wtedy, zamiast ciągłego
przyspieszania, mogłoby dojść do zwiększenia liczby jednocześnie spadających
bloków. Ale na razie poczekam i zobaczę, co użytkownicy myślą o grze do tej pory.

Jestem pewien, że resztę wymyślisz sam.

