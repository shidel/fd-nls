cdrom [opcje]

Narzędzie wsadowe pomagające w ładowaniu sterownika CD/DVD oraz rozszerzeń dysku.

    [brak]          Próba wyszukania odpowiednich sterowników.
    search          Zrób to samo.

    help            Pokaż ten ekran pomocy. ;)

    display         Pokaż aktualnie skonfigurowany napęd CD-ROM.

    NAZWA_STEROWNIKA Nie szukaj. Po prostu spróbuj konkretnego sterownika.
                    (jak UDVD2, GCDROM itp.)

Podczas wyszukiwania podjęta zostanie próba znalezienia i użycia następujących sterowników:

    AHCICD.SYS
    VIDE-CDD.SYS
    OAKCDROM.SYS
    GSCDROM.SYS
    UDVD2.SYS
    ELTORITO.SYS
    GCDROM.SYS
    UIDE.SYS
    ATAPICDD.SYS
