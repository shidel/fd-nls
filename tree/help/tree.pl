To jest plik readme programu FreeDOS TREE.

Graficznie wyświetla strukturę folderów napędu lub ścieżki.

Użycie:  TREE [/A] [/F] [ŚCIEŻKA]
  /A    Użyj znaków ASCII do rysowania linii.
  /F    Wyświetla pliki w katalogach (zamiast samych katalogów).
  ŚCIEŻKA Gdzie zacząć wyświetlanie drzewa katalogów, domyślnie jest
        to bieżący katalog.

Ten program posiada wsparcie międzynarodowe przy użyciu biblioteki "Cats"
(implementacja catopen/catgets dla DOS). Możesz pobrać "Cats" z
http://www.freedos.org/jhall/cats

